<script setup>
import { ref, reactive, onMounted } from 'vue'
import emailjs from '@emailjs/browser'

// Particle system
const particles = ref([])
const createParticles = () => {
  const particleCount = window.innerWidth < 640 ? 20 : 40
  for (let i = 0; i < particleCount; i++) {
    particles.value.push({
      x: Math.random() * window.innerWidth,
      y: Math.random() * window.innerHeight,
      size: Math.random() * (window.innerWidth < 640 ? 15 : 25) + 5,
      opacity: Math.random() * 0.4 + 0.1,
      duration: Math.random() * 25 + 10,
      delay: Math.random() * 8
    })
  }
}

// Typing effect
const messages = [
  "Ready to collaborate on something extraordinary?",
  "Let's turn your ideas into digital reality.",
  "Your next project starts with a conversation."
]
const typingText = ref('')
const typingIndex = ref(0)
const typingCharIndex = ref(0)
const isTyping = ref(true)

const typeWriter = () => {
  if (typingCharIndex.value < messages[typingIndex.value].length) {
    typingText.value += messages[typingIndex.value].charAt(typingCharIndex.value)
    typingCharIndex.value++
    setTimeout(typeWriter, Math.random() * 50 + 50)
  } else {
    isTyping.value = false
    setTimeout(eraseText, 2000)
  }
}

const eraseText = () => {
  if (typingText.value.length > 0) {
    typingText.value = typingText.value.substring(0, typingText.value.length - 1)
    setTimeout(eraseText, 30)
  } else {
    isTyping.value = true
    typingIndex.value = (typingIndex.value + 1) % messages.length
    typingCharIndex.value = 0
    setTimeout(typeWriter, 500)
  }
}

// Form handling
const form = reactive({
  name: '',
  email: '',
  message: ''
})
const activeField = ref(null)
const isSubmitting = ref(false)
const showConfetti = ref(false)
const showSuccessModal = ref(false)
const hoveredSocial = ref(null)
const modalHover = ref(false)

const handleSubmit = async () => {
  if (!form.name || !form.email || !form.message) {
    // Add shake animation to empty fields
    if (!form.name) document.getElementById('name').classList.add('animate-shake')
    if (!form.email) document.getElementById('email').classList.add('animate-shake')
    if (!form.message) document.getElementById('message').classList.add('animate-shake')
    
    setTimeout(() => {
      document.getElementById('name')?.classList.remove('animate-shake')
      document.getElementById('email')?.classList.remove('animate-shake')
      document.getElementById('message')?.classList.remove('animate-shake')
    }, 1000)
    
    return
  }

  isSubmitting.value = true

  try {
    // Send email using EmailJS
    await emailjs.send(
      'service_fm5efop', // Replace with your service ID
      'template_8ocv8ui', // Replace with your template ID
      {
        from_name: form.name,
        from_email: form.email,
            to_email: '',
        message: form.message
      },
      'xTRHA_ddfEUW4fM-t' // Replace with your public key
    )

    // Success effects
    showConfetti.value = true
    showSuccessModal.value = true
    setTimeout(() => showConfetti.value = false, 3000)
    
    // Reset form
    form.name = ''
    form.email = ''
    form.message = ''
  } catch (error) {
    console.error('Failed to send email:', error)
    // Add error animation
    const button = document.querySelector('button[type="submit"]')
    button.classList.add('animate-error')
    setTimeout(() => button.classList.remove('animate-error'), 1000)
  } finally {
    isSubmitting.value = false
  }
}

// Social links with colors
const socialLinks = [
  { icon: 'fab fa-github', url: 'https://github.com/gathogo', color: '#6e5494' },
  { icon: 'fab fa-linkedin-in', url: 'https://linkedin.com/in/gathogo', color: '#0077b5' },
  { icon: 'fab fa-twitter', url: 'https://twitter.com/gathogo', color: '#1DA1F2' },
  { icon: 'fab fa-discord', url: 'https://discord.com/users/gathogo', color: '#5865F2' }
]

// Confetti colors
const confettiColors = [
  '#3b82f6', '#8b5cf6', '#ec4899', '#f43f5e', '#f59e0b', '#10b981'
]

onMounted(() => {
  createParticles()
  setTimeout(typeWriter, 1000)
  
  // Responsive adjustments
  window.addEventListener('resize', () => {
    particles.value = []
    createParticles()
  })
})
</script>

